package com.st.variable;

public class VariableDemo1 {
    public static void main(String[] args) {
        // 1. 定义一个整形变量
        int  age=21;
        // 2. 定义一个小数变量
        double score = 99.8;
        // 输出此变量的值
        System.out.println(age);
        System.out.println(score);
        System.out.println("--------------------------");
        score = score + 1 - 5;
        System.out.println(score);
    }
}
